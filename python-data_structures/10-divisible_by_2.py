#!/usr/bin/python3
def divisible_by_2(my_list=[]):
    list_len = len(my_list)
    for i in list_len:
        if list_len[i] % 2 == 0:
            while list_len < 0:
                new_list = list_len[True]
                return new_list
            else:
                new_list = list_len[False]
                return new_list
